    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        
        try {
            ModeloEntradas me = new ModeloEntradas();
            me.setCedula(Long.parseLong(codigou.getText()));
            me.setFecha(fecha.getText());
            ControlEntradas ce = new ControlEntradas();
            ce.buscar(me);
            tipo.setSelectedItem(me.getTipodoc());
            nombre.setText(me.getNombre());
            apellido.setText(me.getApellido());
            telefono.setText(me.getTelefono());
            direccion.setText(me.getDireccion());
            ciudad.setText(me.getCiudad());
            cargo.setSelectedItem(me.getCargo());
            if(!me.getPrograma().equals("null")){
                programa.setSelectedItem(me.getPrograma());
            }else{
                programa.setSelectedItem("Ninguno");
            }
            guardar1.setEnabled(true);
            cancelar1.setEnabled(true);
        }catch (NumberFormatException ex1) {
            Dialogos dg = new Dialogos(null,"Debe ingresar un valor numerico","");
            dg.setVisible(true);
        }
        catch (Exception ex) {
            if(ex.getMessage().equals("El usuario no existe")){
                boolean aceptar = false;
                Mensaje ms = new Mensaje(null,"El usuario no existe.","Desea crear el usuario?");
                ms.setVisible(true);
                aceptar = ms.confirmar;
                if(aceptar==true){
                    programa.setEnabled(true);
                    crear.setVisible(true);
                    cancelar1.setEnabled(true);
                    tipo.setEnabled(true);
                    nombre.setEditable(true);
                    apellido.setEditable(true);
                    telefono.setEditable(true);
                    direccion.setEditable(true);
                    ciudad.setEditable(true);
                    cargo.setEnabled(true);
                    cor.setVisible(true);
                    correo.setVisible(true);
                    temp.setVisible(false);
                    temperatura.setVisible(false);
                    hor.setVisible(false);
                    hora.setVisible(false);
                    carn.setVisible(true);
                    carnet.setVisible(true);
                }
            }else{
                Dialogos dg = new Dialogos(null,ex.getMessage(),"");
                dg.setVisible(true);
            }
        }
    }//GEN-LAST:event_buscarActionPerformed

