    private void guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardar1ActionPerformed
        ModeloEntradas me = new ModeloEntradas();
        try {
            if (Integer.parseInt(temperatura.getText()) < 37) {
                try {
                    me.setFecha(fecha.getText());
                    me.setHora(hora.getText());
                    me.setTemperatura(temperatura.getText());
                    me.setCedula(Long.parseLong(codigou.getText()));
                    me.setCedulafuncionario(Long.parseLong(jLabel4.getText()));
                    ControlEntradas ce = new ControlEntradas();
                ce.crearEntrada(me);
                    Dialogos dg = new Dialogos(null, "La entrada fue creada exitosamente", "");
                    dg.setVisible(true);
                    dispose();
                } catch (Exception e) {
                    Dialogos dg = new Dialogos(null, e.getMessage(), "");
                    dg.setVisible(true);
                }
            } else {
                Dialogos dg = new Dialogos(null, "Temperatura alta, se registrara", "una novedad");
                dg.setVisible(true);
                Novedad nv = new Novedad(null, codigou.getText(), nombre.getText(), temperatura.getText(), jLabel4.getText());
                nv.setVisible(true);
                dispose();
            }
        } catch (NumberFormatException ex) {
            if(temperatura.getText().matches("\\d*")==false){
            Dialogos dg = new Dialogos(null, "La temperatura debe ser un valor", "numerico");
            dg.setVisible(true);
            }else{
            Dialogos dg = new Dialogos(null, "Por favor ingresar la temperatura", "");
            dg.setVisible(true);
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_guardar1ActionPerformed

