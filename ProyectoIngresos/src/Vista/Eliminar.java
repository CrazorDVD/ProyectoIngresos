    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        try {
            ModeloEliminar me = new ModeloEliminar();
            me.setCedula(cedula.getText());
            ControlEliminar ce = new ControlEliminar();
            ce.buscarUsuario(me);
            tipodoc.setSelectedItem(me.getTipodoc());
            nombre.setText(me.getNombre());
            apellido.setText(me.getApellido());
            carnet.setText(me.getCarnet());
            cancelar.setEnabled(true);
            eliminar.setEnabled(true);
            cedula.setEditable(false);
        } catch (Exception e) {
            Dialogos d = new Dialogos(null,e.getMessage(),"");
            d.setVisible(true);
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarActionPerformed
            tipodoc.setSelectedItem("C.C");
            nombre.setText("");
            apellido.setText("");
            carnet.setText("");
            cedula.setText("");
            cedula.setEditable(true);
            cedula.requestFocus();
            cancelar.setEnabled(false);
            eliminar.setEnabled(false);
    }//GEN-LAST:event_cancelarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        try {
            boolean aceptar = false;
            ModeloEliminar me = new ModeloEliminar();
            me.setCedula(cedula.getText());
            Mensaje m = new Mensaje(null,"Desea eliminar su cuenta","de manera inteligente?");
            m.setVisible(true);
            aceptar = m.confirmar;
            if (aceptar == true) {
                ControlEliminar ce = new ControlEliminar();
                ce.eliminarUsuario(me);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_eliminarActionPerformed

