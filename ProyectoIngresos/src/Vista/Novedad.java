    private void enfermActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enfermActionPerformed

         if(enfermedades.getText().equals("")){

                enfermedades.setText(""+enferm.getSelectedItem());

                if (contadorap >= 0){
                    enferm.removeItem(enferm.getSelectedItem());
                    if(enferm.getItemCount()==0){
                        enferm.setVisible(false);
                    }
                }
                contadorap++;
            }
        
    }//GEN-LAST:event_enfermActionPerformed

   

