    private void guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardar1ActionPerformed
        
        try {
            String enfermedade="";
        
            for (int i = 0; i < enferm.getItemCount(); i++) {
                enfermedade = enfermedade+enferm.getItemAt(i)+",";
            }
            if(!"".equals(enfermedades.getText())){
                enfermedade = enfermedade+enfermedades.getText();
            }else{
                enfermedade = enfermedade.substring(0, enfermedade.length()-1);
            }
            ModeloNovedad mn = new ModeloNovedad();
            mn.setDocumento(codigou.getText());
            mn.setTemperatura(temperatura.getText());
            mn.setEnfermedades(enfermedade);
            mn.setConviveme(""+convive.getSelectedItem());
            mn.setUltimositio(ultimosi.getText());
            mn.setCedulafunc(cedulafun);
            ControlNovedad cn = new ControlNovedad();
            cn.crearNovedad(mn);           
            Dialogos d = new Dialogos(null,"Novedad creada exitosanente","");
            d.setVisible(true);
            dispose();
            
        } catch (Exception e) {
            Dialogos d = new Dialogos(null,e.getMessage(),"");
            d.setVisible(true);
        }
        
    }//GEN-LAST:event_guardar1ActionPerformed

    private void masActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_masActionPerformed
 
        if (!enfermedades.getText().equals("")) {
            enferm.setVisible(true);
            if (enferm.getItemCount() != 0) {
                boolean esta = false;
                for (int i = 0; i < enferm.getItemCount(); i++) {
                    if (enferm.getItemAt(i).equals(enfermedades.getText())) {
                        Dialogos d = new Dialogos(null, "La enfermedad ya se encuentra", "registrada en esta novedad");
                        d.setVisible(true);
                        esta = true;
                    }
                }
                if (esta == false) {
                    enferm.addItem(enfermedades.getText());
                    enfermedades.setText("");
                }
            } else {
                enferm.addItem(enfermedades.getText());
                enfermedades.setText("");
            }
        }else{
            Dialogos d = new Dialogos(null, "Por favor ingrese una enfermedad", "");
            d.setVisible(true);
        }
        
        
        
        
    }//GEN-LAST:event_masActionPerformed

